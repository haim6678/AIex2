public class Point {


	private int x;
	private int y;
	private int huristic;

	/**
	 * the constructor
	 */
	public Point(int x, int y, int h) {
		this.x = x;
		this.y = y;
		this.huristic = h;
	}

	/**
	 * getters
	 */
	public int getX() {
		return x;
	}

	public int getY() {
		return y;
	}

	public int getHuristic() {
		return huristic;
	}

}
